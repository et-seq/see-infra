import { readdir, writeFile } from "node:fs/promises";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const destinationsDir = join(repoRoot, "src", "redirects", "destinations");
const manifestPath = join(destinationsDir, "manifest.generated.ts");

const ignoredFiles = new Set(["index.ts", "manifest.generated.ts"]);

const destinationFiles = (await readdir(destinationsDir, { withFileTypes: true }))
  .filter((entry) => entry.isFile())
  .map((entry) => entry.name)
  .filter((fileName) => fileName.endsWith(".ts"))
  .filter((fileName) => !fileName.endsWith(".d.ts"))
  .filter((fileName) => !fileName.endsWith(".test.ts"))
  .filter((fileName) => !ignoredFiles.has(fileName))
  .sort((left, right) => left.localeCompare(right));

const imports = destinationFiles
  .map((fileName, index) => {
    const modulePath = `./${fileName.slice(0, -".ts".length)}`;

    return `import destination${index} from "${modulePath}";`;
  })
  .join("\n");

const entries = destinationFiles
  .map((_, index) => `  destination${index},`)
  .join("\n");

const manifest = `// Generated by scripts/generate-destination-manifest.mjs.
// Add destination files in this directory; do not edit this file by hand.

import type { RedirectDefinition } from "../types";
${imports ? `\n${imports}\n` : ""}
export const redirectDefinitions = [
${entries ? `${entries}\n` : ""}] as const satisfies readonly RedirectDefinition[];
`;

await writeFile(manifestPath, manifest);

const relativeManifestPath = relative(repoRoot, manifestPath);
const noun = destinationFiles.length === 1 ? "destination" : "destinations";

console.log(
  `Generated ${relativeManifestPath} with ${destinationFiles.length} ${noun}.`,
);
